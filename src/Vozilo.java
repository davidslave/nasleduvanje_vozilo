
public class Vozilo {
	private int brzina;
	private int kapacitetGorivo;

	public Vozilo(int brzina, int kapacitetGorivo) {
		this.brzina = brzina;
		this.kapacitetGorivo = kapacitetGorivo;
	}

	public int getBrzina() {
		return brzina;
	}

	public void setBrzina(int brzina) {
		this.brzina = brzina;
	}

	public int getKapacitetGorivo() {
		return kapacitetGorivo;
	}

	public void setKapacitetGorivo(int kapacitetGorivo) {
		this.kapacitetGorivo = kapacitetGorivo;
	}
}

